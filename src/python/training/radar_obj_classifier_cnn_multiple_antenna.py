def save_model(model, n_ant, acc):
    """Save model weights and metadata to disk."""
    filepath = f'radar_shape_model_N{n_ant}.pt'
    torch.save({
        'model_state_dict': model.state_dict(),
        'class_names'     : CLASS_NAMES,
        'n_classes'       : N_CLASSES,
        'n_ant'           : n_ant,
        'test_accuracy'   : acc,
    }, filepath)
    print(f"  Model saved: {filepath}")
    return filepath


def load_model(filepath):
    """Load a saved model for inference."""
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"Model not found: {filepath}")

    ckpt  = torch.load(filepath, map_location=device)
    model = ShapeCNN(n_classes=ckpt['n_classes'])
    model.load_state_dict(ckpt['model_state_dict'])
    model = model.to(device)
    model.eval()

    print(f"Loaded : {filepath}")
    print(f"  N_ant    : {ckpt.get('n_ant', 'unknown')}")
    print(f"  Accuracy : {ckpt.get('test_accuracy', 0)*100:.1f}%")

    return model, ckpt


