def train_model(model, train_dl, val_dl, n_ant,
                n_epochs=40, lr=1e-3, patience=20, warmup_epochs=3):
    """
    Train one model on one dataset.

    Args:
        model         : ShapeCNN instance (freshly initialised)
        train_dl      : training DataLoader
        val_dl        : validation DataLoader
        n_ant         : antenna count (used for print labels only)
        n_epochs      : max training epochs
        lr            : target learning rate (after warmup)
        patience      : early stopping patience in epochs
        warmup_epochs : linear warmup length (0.1*lr -> lr)

    Returns:
        model   : best weights restored
        history : loss and accuracy per epoch

    Notes:
        Patience is set high (20) because the EMA val loss in this
        setup transiently spikes 2-3x mid-training before recovering,
        especially at high N_ant. A 3-epoch linear warmup suppresses
        the worst of those spikes by letting BatchNorm statistics
        settle before the LR hits its target.
    """
    model     = model.to(device)
    criterion = nn.CrossEntropyLoss()
    optimizer = optim.Adam(model.parameters(),
                           lr=lr, weight_decay=1e-4)
    # LR schedule: linear warmup for `warmup_epochs`, then cosine
    # annealing from lr down to 1e-6 over the remaining epochs.
    # Warmup reduces the early-epoch val-loss spikes caused by
    # BatchNorm running-stat instability, which had been triggering
    # premature early-stopping at N_ant >= 10.
    warmup = optim.lr_scheduler.LinearLR(
        optimizer, start_factor=0.1, end_factor=1.0,
        total_iters=max(warmup_epochs, 1),
    )
    cosine = optim.lr_scheduler.CosineAnnealingLR(
        optimizer, T_max=max(n_epochs - warmup_epochs, 1),
        eta_min=1e-6,
    )
    scheduler = optim.lr_scheduler.SequentialLR(
        optimizer,
        schedulers=[warmup, cosine],
        milestones=[warmup_epochs],
    )

    history = {
        'train_loss'  : [], 'val_loss'    : [],
        'train_acc'   : [], 'val_acc'     : [],
        'val_loss_ema': [], 'val_acc_ema' : [],
    }
    ema_val_loss = None
    ema_val_acc  = None
    EMA_ALPHA    = 0.3          # 0 = fully smoothed, 1 = no smoothing

    best_val   = float('inf')
    best_wts   = None
    no_improve = 0

    print(f"\n{'='*62}")
    print(f"  Training | N_ant={n_ant} | {n_epochs} epochs | lr={lr}")
    print(f"{'='*62}")
    print(f"{'Ep':>4} | {'TrLoss':>8} | {'VaLoss':>8} | "
          f"{'TrAcc':>7} | {'VaAcc':>7} | {'LR':>9}")
    print(f"{'-'*62}")

    for ep in range(1, n_epochs + 1):
        t0 = time.time()

        # -- Train phase --
        model.train()
        tr_loss = tr_corr = tr_n = 0

        for Xb, Yb in train_dl:
            Xb, Yb = Xb.to(device), Yb.to(device)
            # Random horizontal flip with 50% probability per sample
            flip_mask = torch.rand(Xb.size(0)) > 0.5
            Xb[flip_mask] = torch.flip(Xb[flip_mask], dims=[-1])
            optimizer.zero_grad()
            out  = model(Xb)
            loss = criterion(out, Yb)
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
            optimizer.step()

            tr_loss += loss.item() * Xb.size(0)
            tr_corr += (out.argmax(1) == Yb).sum().item()
            tr_n    += Xb.size(0)

        tr_loss /= tr_n
        tr_acc   = tr_corr / tr_n

        # -- Validation phase --
        model.eval()
        va_loss = va_corr = va_n = 0

        with torch.no_grad():
            for Xb, Yb in val_dl:
                Xb, Yb = Xb.to(device), Yb.to(device)
                out      = model(Xb)
                loss     = criterion(out, Yb)
                va_loss += loss.item() * Xb.size(0)
                va_corr += (out.argmax(1) == Yb).sum().item()
                va_n    += Xb.size(0)

        va_loss /= va_n
        va_acc   = va_corr / va_n

        # -- EMA smoothing of val metrics --
        # Damps per-epoch noise so training curves and early stopping
        # react to trends, not to single-batch spikes.
        if ema_val_loss is None:
            ema_val_loss = va_loss
            ema_val_acc  = va_acc
        else:
            ema_val_loss = EMA_ALPHA * va_loss + (1 - EMA_ALPHA) * ema_val_loss
            ema_val_acc  = EMA_ALPHA * va_acc  + (1 - EMA_ALPHA) * ema_val_acc

        scheduler.step()
        cur_lr = optimizer.param_groups[0]['lr']

        history['train_loss'].append(tr_loss)
        history['val_loss'].append(va_loss)
        history['train_acc'].append(tr_acc)
        history['val_acc'].append(va_acc)
        history['val_loss_ema'].append(ema_val_loss)
        history['val_acc_ema'].append(ema_val_acc)

        print(f"{ep:>4} | {tr_loss:>8.4f} | {va_loss:>8.4f} | "
              f"{tr_acc*100:>6.1f}% | {va_acc*100:>6.1f}% | "
              f"{cur_lr:>9.6f}  ({time.time()-t0:.1f}s)  "
              f"EMA_VaLoss={ema_val_loss:.4f}")

        # -- Early stopping (on smoothed val loss, not raw) --
        if ema_val_loss < best_val:
            best_val   = ema_val_loss
            best_wts   = {k: v.clone()
                          for k, v in model.state_dict().items()}
            no_improve = 0
        else:
            no_improve += 1
            if no_improve >= patience:
                print(f"\n  Early stopping at epoch {ep}.")
                break

    model.load_state_dict(best_wts)
    print(f"\n  Best val loss: {best_val:.4f}")
    return model, history


# ============================================================
#  SECTION 4: EVALUATION
# ============================================================

def evaluate_model(model, test_dl, n_ant):
    """Evaluate on test set and plot confusion matrix."""

    model.eval()
    all_preds  = []
    all_labels = []

    with torch.no_grad():
        for Xb, Yb in test_dl:
            out = model(Xb.to(device))
            all_preds.extend(out.argmax(1).cpu().numpy())
            all_labels.extend(Yb.numpy())

    preds  = np.array(all_preds)
    labels = np.array(all_labels)
    acc    = float(np.mean(preds == labels))

    print(f"\n  Test Accuracy (N_ant={n_ant}): {acc*100:.2f}%\n")
    print(classification_report(labels, preds,
                                 target_names=CLASS_NAMES,
                                 digits=3))

    # Confusion matrix plots
    cm      = confusion_matrix(labels, preds)
    cm_norm = cm.astype(float) / cm.sum(axis=1, keepdims=True)

    fig, axes = plt.subplots(1, 2, figsize=(13, 5))
    fig.suptitle(
        f'Confusion Matrix  |  N_ant={n_ant}  |  '
        f'Accuracy={acc*100:.1f}%',
        fontsize=13, fontweight='bold'
    )

    for ax, data, fmt, title in zip(
        axes,
        [cm, cm_norm],
        ['d', '.1%'],
        ['Counts', 'Normalised']
    ):
        sns.heatmap(data, annot=True, fmt=fmt, cmap='Blues',
                    xticklabels=CLASS_NAMES,
                    yticklabels=CLASS_NAMES,
                    ax=ax, linewidths=0.5, square=True)
        ax.set_xlabel('Predicted', fontsize=11)
        ax.set_ylabel('True',      fontsize=11)
        ax.set_title(title,        fontsize=11)

    plt.tight_layout()
    fname = f'confusion_N{n_ant}.png'
    plt.savefig(fname, dpi=150, bbox_inches='tight')
    plt.show()
    print(f"  Saved: {fname}")

    return acc, preds, labels


