def per_class_accuracy(preds, labels):
    """Return accuracy dict keyed by class name."""
    acc_dict = {}
    for i, name in enumerate(CLASS_NAMES):
        mask = labels == i
        if mask.sum() > 0:
            acc_dict[name] = float(
                np.mean(preds[mask] == labels[mask])
            )
        else:
            acc_dict[name] = 0.0
    return acc_dict


