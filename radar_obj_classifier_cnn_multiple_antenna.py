def load_dataset(filepath, batch_size=128):
    """
    Load one MATLAB dataset file and return DataLoaders.

    Args:
        filepath   : path to .mat file
        batch_size : samples per training batch

    Returns:
        train_dl, val_dl, test_dl : PyTorch DataLoaders
        meta                      : dict with n_ant and filepath
    """
    if not os.path.exists(filepath):
        raise FileNotFoundError(
            f"\nDataset not found: {filepath}\n"
            f"Run radar_shape_simple.m in MATLAB first.\n"
            f"Make sure the antenna loop generates all four files."
        )

    print(f"\nLoading: {filepath}")

    # Try scipy first (works for MAT files up to v7.2).
    # Fall back to h5py for v7.3 (HDF5) files.
    try:
        data = scipy.io.loadmat(filepath)
        def prep(X, Y):
            X = np.expand_dims(X.astype(np.float32), axis=1)
            Y = Y.flatten().astype(np.int64) - 1
            return torch.tensor(X), torch.tensor(Y)
        X_tr, Y_tr = prep(data['X_train'], data['Y_train'])
        X_vl, Y_vl = prep(data['X_val'],   data['Y_val'])
        X_te, Y_te = prep(data['X_test'],  data['Y_test'])
        _n_ant_raw = data.get('N_ant', None)
        n_ant_from_file = int(np.array(_n_ant_raw).flatten()[0]) if _n_ant_raw is not None else None
    except NotImplementedError:
        # v7.3 HDF5 format — arrays are stored transposed relative to scipy
        with h5py.File(filepath, 'r') as f:
            def prep_h5(key_X, key_Y):
                X = np.array(f[key_X], dtype=np.float32)
                Y = np.array(f[key_Y], dtype=np.int64)
                # h5py transposes MATLAB arrays: shape is (cols, rows, ...)
                # For 2-D data stored as (range, angle, N) -> transpose to (N, range, angle)
                if X.ndim == 3:
                    X = X.transpose(2, 0, 1)   # (range, angle, N) -> (N, range, angle)
                elif X.ndim == 2:
                    X = X.T                     # (features, N) -> (N, features)
                X = np.expand_dims(X, axis=1)   # add channel dim
                Y = Y.flatten() - 1             # 1-indexed -> 0-indexed
                return torch.tensor(X), torch.tensor(Y)
            X_tr, Y_tr = prep_h5('X_train', 'Y_train')
            X_vl, Y_vl = prep_h5('X_val',   'Y_val')
            X_te, Y_te = prep_h5('X_test',  'Y_test')
            n_ant_from_file = int(np.array(f['N_ant']).flatten()[0]) if 'N_ant' in f else None

    # Read N_ant from file if saved, otherwise parse from filename
    if n_ant_from_file is not None:
        n_ant = n_ant_from_file
    else:
        basename = os.path.basename(filepath)
        try:
            n_ant = int(''.join(
                filter(str.isdigit,
                       basename.split('N')[-1].split('.')[0])
            ))
        except Exception:
            n_ant = -1

    print(f"  N_ant  : {n_ant}")
    print(f"  Train  : {X_tr.shape}")
    print(f"  Val    : {X_vl.shape}")
    print(f"  Test   : {X_te.shape}")
    print(f"\n  Class distribution (train):")
    for i, name in enumerate(CLASS_NAMES):
        n = (Y_tr == i).sum().item()
        bar = 'x' * (n // 10)
        print(f"    {i+1}. {name:12s}: {n:4d}  {bar}")

    train_dl = DataLoader(TensorDataset(X_tr, Y_tr),
                          batch_size=batch_size,
                          shuffle=True, drop_last=True)
    val_dl   = DataLoader(TensorDataset(X_vl, Y_vl),
                          batch_size=batch_size, shuffle=False)
    test_dl  = DataLoader(TensorDataset(X_te, Y_te),
                          batch_size=batch_size, shuffle=False)

    meta = {
        'n_ant'   : n_ant,
        'filepath': filepath,
        'n_train' : len(X_tr),
        'n_val'   : len(X_vl),
        'n_test'  : len(X_te),
    }

    return train_dl, val_dl, test_dl, meta


# ============================================================
#  SECTION 2: CNN ARCHITECTURE
#
#  Input:  [B x 1 x 512 x 256]
#  Output: [B x 5]
#
#  4 conv blocks -> Global Average Pooling -> 2 FC layers.
#  Same architecture used for ALL antenna experiments so
#  that accuracy differences reflect the data, not the model.
# ============================================================

class ShapeCNN(nn.Module):
    """
    2D CNN for radar shape classification.
    Architecture is fixed across all antenna experiments.
    Only the training data changes between experiments.
    """

    def __init__(self, n_classes=5):
        super().__init__()

        self.features = nn.Sequential(

            # Block 1: 7x7 kernel - detect broad shape structure
            nn.Conv2d(1, 16, kernel_size=7, padding=3),
            nn.BatchNorm2d(16),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),         # 512x256 -> 256x128

            # Block 2: 5x5 kernel - mid-level features
            nn.Conv2d(16, 32, kernel_size=5, padding=2),
            nn.BatchNorm2d(32),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),         # 256x128 -> 128x64

            # Block 3: 3x3 kernel - fine structure
            nn.Conv2d(32, 64, kernel_size=3, padding=1),
            nn.BatchNorm2d(64),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),         # 128x64 -> 64x32

            # Block 4: 3x3 kernel - high-level combinations
            nn.Conv2d(64, 128, kernel_size=3, padding=1),
            nn.BatchNorm2d(128),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2, 2),         # 64x32 -> 32x16
        )

        # Global Average Pooling: [B x 128 x 32 x 16] -> [B x 128 x 1 x 1]
        self.gap = nn.AdaptiveAvgPool2d(1)

        self.classifier = nn.Sequential(
            nn.Flatten(),               # [B x 128 x 1 x 1] -> [B x 128]
            nn.Linear(128, 64),
            nn.ReLU(inplace=True),
            nn.Dropout(0.4),
            nn.Linear(64, n_classes),   # [B x 64] -> [B x 5]
        )

    def forward(self, x):
        x = self.features(x)
        x = self.gap(x)
        x = self.classifier(x)
        return x


