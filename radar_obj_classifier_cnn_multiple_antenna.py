def predict_shape(heatmap, model, true_label=None):
    """
    Predict the shape of one radar heatmap.

    Args:
        heatmap   : numpy array [range_bins x angle_bins]
        model     : trained ShapeCNN
        true_label: int 0-4 if known (for display only)

    Returns:
        pred_name  : predicted shape name
        confidence : float 0-1
        all_probs  : dict of class name -> probability
    """
    model.eval()

    hm = heatmap.astype(np.float32)
    if hm.max() > 0:
        hm = hm / hm.max()

    # Resize to training dimensions if needed
    if hm.shape != (512, 256):
        from scipy.ndimage import zoom
        hm = zoom(hm, (512/hm.shape[0], 256/hm.shape[1]), order=1)
        hm = hm / (hm.max() + 1e-9)

    tensor = torch.tensor(hm).unsqueeze(0).unsqueeze(0).to(device)

    with torch.no_grad():
        logits = model(tensor)
        probs  = torch.softmax(logits, dim=1).cpu().numpy()[0]

    pred_idx   = int(np.argmax(probs))
    pred_name  = CLASS_NAMES[pred_idx]
    confidence = float(probs[pred_idx])
    all_probs  = {CLASS_NAMES[i]: float(probs[i])
                  for i in range(N_CLASSES)}

    # Console output
    print(f"\n{'='*45}")
    print(f"  RADAR SHAPE PREDICTION")
    print(f"{'='*45}")
    if true_label is not None:
        correct = 'CORRECT' if pred_idx == true_label else 'WRONG'
        print(f"  True shape  : {CLASS_NAMES[true_label]}")
        print(f"  Prediction  : {pred_name}  [{correct}]")
    else:
        print(f"  Prediction  : {pred_name}")
    print(f"  Confidence  : {confidence*100:.1f}%")
    print(f"\n  All probabilities:")
    for name, prob in sorted(all_probs.items(),
                              key=lambda x: -x[1]):
        bar = 'X' * int(prob * 30)
        print(f"    {name:12s}: {prob*100:5.1f}%  {bar}")
    print(f"{'='*45}\n")

    # Visualisation
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(11, 4))

    title_str = f"Prediction: {pred_name} ({confidence*100:.1f}%)"
    if true_label is not None:
        correct = pred_idx == true_label
        tag     = 'CORRECT' if correct else 'WRONG'
        title_str += f"  --  {tag}"
    fig.suptitle(title_str, fontsize=13, fontweight='bold')

    im = ax1.imshow(20*np.log10(hm + 1e-6),
                    cmap='jet', aspect='auto',
                    vmin=-40, vmax=0)
    plt.colorbar(im, ax=ax1, label='dB')
    ax1.set_xlabel('Angle bins')
    ax1.set_ylabel('Range bins')
    ax1.set_title('Input Heatmap')

    names      = list(all_probs.keys())
    probs_list = list(all_probs.values())
    colors     = [CLASS_COLORS[CLASS_NAMES.index(n)] for n in names]
    bars       = ax2.barh(names,
                          [p*100 for p in probs_list],
                          color=colors,
                          edgecolor='white',
                          height=0.6)
    bars[pred_idx].set_edgecolor('black')
    bars[pred_idx].set_linewidth(2.5)
    ax2.set_xlabel('Confidence (%)')
    ax2.set_xlim([0, 115])
    ax2.set_title('Class Probabilities')
    ax2.grid(axis='x', alpha=0.3)
    ax2.axvline(50, color='gray', linestyle='--', alpha=0.4)
    for b, p in zip(bars, probs_list):
        ax2.text(b.get_width()+1,
                 b.get_y() + b.get_height()/2,
                 f'{p*100:.1f}%', va='center', fontsize=9)

    plt.tight_layout()
    plt.savefig('shape_prediction.png', dpi=150,
                bbox_inches='tight')
    plt.show()

    return pred_name, confidence, all_probs


# ============================================================
#  SECTION 8: SAVE AND LOAD MODEL
# ============================================================

def save_model(model, n_ant, acc):
    """Save model weights and metadata to disk."""
    filepath = f'radar_shape_model_N{n_ant}.pt'
    torch.save({
        'model_state_dict': model.state_dict(),
        'class_names'     : CLASS_NAMES,
        'n_classes'       : N_CLASSES,
        'n_ant'           : n_ant,
        'test_accuracy'   : acc,
    }, filepath)
    print(f"  Model saved: {filepath}")
    return filepath


def load_model(filepath):
    """Load a saved model for inference."""
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"Model not found: {filepath}")

    ckpt  = torch.load(filepath, map_location=device)
    model = ShapeCNN(n_classes=ckpt['n_classes'])
    model.load_state_dict(ckpt['model_state_dict'])
    model = model.to(device)
    model.eval()

    print(f"Loaded : {filepath}")
    print(f"  N_ant    : {ckpt.get('n_ant', 'unknown')}")
    print(f"  Accuracy : {ckpt.get('test_accuracy', 0)*100:.1f}%")

    return model, ckpt


# ============================================================
#  SECTION 9: PER-CLASS ACCURACY HELPER
# ============================================================

def per_class_accuracy(preds, labels):
    """Return accuracy dict keyed by class name."""
    acc_dict = {}
    for i, name in enumerate(CLASS_NAMES):
        mask = labels == i
        if mask.sum() > 0:
            acc_dict[name] = float(
                np.mean(preds[mask] == labels[mask])
            )
        else:
            acc_dict[name] = 0.0
    return acc_dict


